import path from 'node:path';

const CANONICAL_INVESTIGATION_PLAN_RE = /^investigation-plan\.md$/i;
const ROOT_MARKDOWN_RE = /^[^/]+\.(?:md|markdown)$/i;
const SUMMARY_SOURCE_DIRS = new Set(['records', 'metrics', 'detections', 'entities']);
const CANONICAL_RESEARCH_SEARCH_RE = /^(?:research|evidence\/research)\/search-[^/]+\.json$/i;
const CANONICAL_RESEARCH_SOURCE_RE = /^(?:research|evidence\/research)\/source-[^/]+\.json$/i;
const CANONICAL_RESEARCH_STATUS_RE = /^(?:research|evidence\/research)\/research-interface-status\.json$/i;
const DIRECT_RESEARCH_JSON_RE = /^(?:research|evidence\/research)\/[^/]+\.json$/i;
const REVERSINGLABS_JSON_RE = /^(?:reversinglabs|research\/reversinglabs|evidence\/research\/reversinglabs)\/[^/]+\.json$/i;
const PACKET_JSON_RE = /^evidence\/packets\/[^/]+\.json$/i;
const ROOT_HTML_RE = /^[^/]+\.(?:html|htm)$/i;
const CANONICAL_REPORT_RE = /^report-[^/]+\.(?:html|htm)$/i;
const DIRECT_RL_HTML_RE = /^(?:reversinglabs|research\/reversinglabs|evidence\/research\/reversinglabs)\/[^/]+\.(?:html|htm)$/i;
const CANONICAL_RL_SUMMARY_RE = /^(?:reversinglabs|research\/reversinglabs|evidence\/research\/reversinglabs)\/rl-summary\.html$/i;
const DIRECT_RESEARCH_MARKDOWN_RE = /^(?:research|evidence\/research)\/[^/]+\.(?:md|markdown)$/i;
const CANONICAL_RESEARCH_SUMMARY_RE = /^(?:research|evidence\/research)\/research-[^/]+\.(?:md|markdown)$/i;

export function normalizeArtifactPath(relPath) {
  return String(relPath || '').replaceAll('\\', '/').replace(/^\/+/, '').toLowerCase();
}

function isPlainObject(value) {
  return Boolean(value) && typeof value === 'object' && !Array.isArray(value);
}

function researchContentKind(value) {
  if (!isPlainObject(value)) return '';
  if (value.kind === 'search' && Array.isArray(value.results)) return 'research-search';
  if (value.kind === 'source' && (
    typeof value.url === 'string' || typeof value.resolvedUrl === 'string' || typeof value.text === 'string'
  )) return 'research-source';
  if (typeof value.ok === 'boolean' && (
    typeof value.effectiveProvider === 'string' || typeof value.provider === 'string'
  )) return 'research-status';
  return '';
}

/**
 * Classify JSON only within approved workspace locations. Canonical names remain
 * useful fallbacks for malformed/partial artifacts, while valid envelopes can
 * recover harmless naming deviations inside those same locations.
 */
export function classifyWorkspaceJsonArtifact(relPath, value) {
  const normalized = normalizeArtifactPath(relPath);
  const parts = normalized.split('/');

  if (parts[0] === 'evidence' && SUMMARY_SOURCE_DIRS.has(parts[1])
    && path.posix.extname(normalized) === '.json') return parts[1];

  if (REVERSINGLABS_JSON_RE.test(normalized)) return 'reversinglabs';
  if (PACKET_JSON_RE.test(normalized)) return 'packet-info';

  if (DIRECT_RESEARCH_JSON_RE.test(normalized)) {
    if (CANONICAL_RESEARCH_SEARCH_RE.test(normalized)) return 'research-search';
    if (CANONICAL_RESEARCH_SOURCE_RE.test(normalized)) return 'research-source';
    if (CANONICAL_RESEARCH_STATUS_RE.test(normalized)) return 'research-status';
    const contentKind = researchContentKind(value);
    if (contentKind) return contentKind;
  }

  return '';
}

export function isWorkspaceJsonArtifactCandidate(relPath) {
  const normalized = normalizeArtifactPath(relPath);
  const parts = normalized.split('/');
  if (path.posix.extname(normalized) !== '.json') return false;
  return (parts[0] === 'evidence' && SUMMARY_SOURCE_DIRS.has(parts[1]))
    || DIRECT_RESEARCH_JSON_RE.test(normalized)
    || REVERSINGLABS_JSON_RE.test(normalized)
    || PACKET_JSON_RE.test(normalized);
}

export function isSummarizableArtifactKind(kind) {
  return SUMMARY_SOURCE_DIRS.has(kind) || kind === 'research-search' || kind === 'reversinglabs';
}

export function classifySummarizableJsonArtifact(relPath, value) {
  const kind = classifyWorkspaceJsonArtifact(relPath, value);
  return isSummarizableArtifactKind(kind) ? kind : '';
}

/** Recover a misnamed root report only when the established report template left
 * two independent markers. Ordinary HTML remains ordinary HTML.
 */
export function isInvestigationReportArtifact(relPath, content = '') {
  const normalized = normalizeArtifactPath(relPath);
  if (CANONICAL_REPORT_RE.test(normalized)) return true;
  if (!ROOT_HTML_RE.test(normalized)) return false;
  const sample = String(content || '');
  return sample.includes('Generated by the ExtraHop Investigation Agent')
    && sample.includes('data-report-theme');
}

export function isReversingLabsSummaryArtifact(relPath, content = '') {
  const normalized = normalizeArtifactPath(relPath);
  if (CANONICAL_RL_SUMMARY_RE.test(normalized)) return true;
  if (!DIRECT_RL_HTML_RE.test(normalized)) return false;
  const sample = String(content || '');
  return sample.includes('ReversingLabs Spectra Analyze')
    && sample.includes('generated from normalized broker responses');
}

export function isResearchSummaryArtifact(relPath, content = '') {
  const normalized = normalizeArtifactPath(relPath);
  if (CANONICAL_RESEARCH_SUMMARY_RE.test(normalized)) return true;
  return DIRECT_RESEARCH_MARKDOWN_RE.test(normalized)
    && /<!--\s*artifact-kind:\s*research-summary\s*-->/i.test(String(content || ''));
}

/**
 * The generated plan projection: the canonical root filename, or any root
 * Markdown carrying the generator's artifact-kind marker (so a renamed copy is
 * still recognized as generated rather than presented as a deliverable).
 */
export function isInvestigationPlanArtifact(relPath, content = '') {
  const normalized = normalizeArtifactPath(relPath);
  if (CANONICAL_INVESTIGATION_PLAN_RE.test(normalized)) return true;
  return ROOT_MARKDOWN_RE.test(normalized)
    && /<!--\s*artifact-kind:\s*investigation-plan\s*-->/i.test(String(content || ''));
}
